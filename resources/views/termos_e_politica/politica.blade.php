            <h1>Política de Privacidade - Portal JE Cursos e Treinamentos LTDA</h1>
    
            <p>Esta Política de Privacidade descreve como suas informações pessoais são coletadas, usadas e compartilhadas quando você visita ou faz uma compra no site <strong>portalje.org</strong>, operado pela Portal JE Cursos e Treinamentos LTDA.</p>
    
            <h2>1. Informações que Coletamos</h2>
            <p>Coletamos diferentes tipos de informações sobre você, incluindo:</p>
            <ul>
                <li><strong>Informações de Identificação Pessoal:</strong> Nome, endereço, email, número de telefone, CPF/CNPJ, etc., fornecidos por você no momento do cadastro ou compra de cursos.</li>
                <li><strong>Informações de Navegação:</strong> Dados de navegação, como endereço IP, tipo de navegador, páginas acessadas e tempo de visita.</li>
                <li><strong>Dados de Pagamento:</strong> Informações necessárias para processar transações, como dados de cartões de crédito e informações bancárias.</li>
            </ul>
        
            <h2>2. Uso das Informações</h2>
            <p>As informações coletadas podem ser usadas para os seguintes propósitos:</p>
            <ul>
                <li>Processar suas transações e fornecer os cursos adquiridos.</li>
                <li>Comunicar-se com você sobre sua conta ou compras realizadas.</li>
                <li>Enviar informações promocionais e ofertas, desde que você tenha consentido previamente.</li>
                <li>Melhorar nosso site e serviços, analisando como os usuários interagem com nossa plataforma.</li>
                <li>Cumprir obrigações legais e regulamentares.</li>
            </ul>
        
            <h2>3. Compartilhamento de Informações</h2>
            <p>Podemos compartilhar suas informações com terceiros apenas nos seguintes casos:</p>
            <ul>
                <li><strong>Fornecedores de Serviços:</strong> Com terceiros que nos auxiliam na operação do site, processamento de pagamentos, entrega de cursos, entre outros.</li>
                <li><strong>Requisitos Legais:</strong> Para cumprir com a lei, regulamento ou ordem judicial.</li>
                <li><strong>Proteção de Direitos:</strong> Para proteger os direitos, propriedade e segurança do Portal JE, nossos usuários ou terceiros.</li>
            </ul>
        
            <h2>4. Segurança das Informações</h2>
            <p>Adotamos medidas de segurança técnicas e organizacionais para proteger suas informações pessoais contra acesso não autorizado, perda ou alteração. No entanto, nenhum sistema é completamente seguro e não podemos garantir a segurança absoluta das informações transmitidas ao nosso site.</p>
        
            <h2>5. Retenção de Dados</h2>
            <p>Manteremos suas informações pelo período necessário para cumprir os propósitos descritos nesta Política de Privacidade, a menos que um período de retenção maior seja exigido ou permitido por lei.</p>
        
            <h2>6. Direitos dos Usuários</h2>
            <p>Você tem o direito de acessar, corrigir, atualizar ou solicitar a exclusão de suas informações pessoais. Para exercer esses direitos, entre em contato conosco através do email <a href="mailto:parceiros@jovemempreendedor.org">parceiros@jovemempreendedor.org.org</a>.</p>
        
            <h2>7. Cookies e Tecnologias Semelhantes</h2>
            <p>Utilizamos cookies e outras tecnologias de rastreamento para melhorar sua experiência no site. Você pode ajustar as configurações do seu navegador para recusar cookies, mas isso pode afetar a funcionalidade do site.</p>
        
            <h2>8. Alterações na Política de Privacidade</h2>
            <p>Podemos atualizar esta Política de Privacidade periodicamente para refletir, por exemplo, mudanças em nossas práticas ou por outras razões operacionais, legais ou regulatórias. Notificaremos sobre quaisquer mudanças significativas através de nosso site ou por outros meios apropriados.</p>
        
            <h2>9. Contato</h2>
            <p>Se você tiver dúvidas sobre esta Política de Privacidade, entre em contato conosco através do email: <a href="mailto:parceiros@jovemempreendedor.org">parceiros@jovemempreendedor.org</a>.</p>
        
            <h2>10. Legislação Aplicável</h2>
            <p>Esta Política de Privacidade será regida e interpretada de acordo com as leis do Brasil. Qualquer disputa que surja desta política será submetida ao foro da comarca de [cidade], [estado].</p>
        
        

