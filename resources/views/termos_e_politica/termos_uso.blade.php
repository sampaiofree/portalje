    <h2>1. Aceitação dos Termos</h2>
    <p>Ao se cadastrar como afiliado no Portal JE Cursos e Treinamentos LTDA, você concorda em cumprir e respeitar todos os termos e condições aqui descritos. Esses termos regulam o uso do programa de afiliados para promover e vender nossos cursos profissionalizantes através do site <strong>portalje.org</strong>.</p>

    <h2>2. Definições</h2>
    <p>Para efeitos deste documento:</p>
    <ul>
        <li><strong>Afiliado</strong>: Pessoa física ou jurídica que se cadastra no programa de afiliados para promover e vender os cursos oferecidos pelo Portal JE.</li>
        <li><strong>Portal JE</strong>: Refere-se ao Portal JE Cursos e Treinamentos LTDA.</li>
        <li><strong>Cliente</strong>: Qualquer pessoa que adquirir um curso através do link de afiliado.</li>
    </ul>

    <h2>3. Requisitos para Participação</h2>
    <p>Para participar do programa de afiliados, o candidato deve:</p>
    <ul>
        <li>Ter mais de 18 anos.</li>
        <li>Possuir um CPF ou CNPJ válido.</li>
        <li>Fornecer informações precisas e verdadeiras durante o processo de cadastro.</li>
    </ul>

    <h2>4. Direitos e Responsabilidades dos Afiliados</h2>
    <h3>4.1. Promoção dos Cursos</h3>
    <p>O afiliado pode promover os cursos utilizando qualquer meio de divulgação permitido por lei, desde que em conformidade com as políticas de publicidade da Meta e outras plataformas digitais utilizadas.</p>

    <h3>4.2. Conduta Ética</h3>
    <p>O afiliado deve agir de maneira ética e profissional, não utilizando práticas enganosas, fraudulentas ou quaisquer métodos que possam prejudicar a reputação do Portal JE.</p>

    <h3>4.3. Material Promocional</h3>
    <p>O afiliado tem acesso a materiais promocionais fornecidos pelo Portal JE, incluindo banners, links e textos publicitários. Todo o material deve ser utilizado conforme as diretrizes fornecidas.</p>

    <h3>4.4. Proibições</h3>
    <p>O afiliado não deve:</p>
    <ul>
        <li>Modificar os links de afiliado fornecidos pelo Portal JE.</li>
        <li>Utilizar técnicas de spam ou envio não autorizado de emails.</li>
        <li>Fazer declarações falsas ou enganosas sobre os cursos do Portal JE.</li>
    </ul>

    <h2>5. Comissão e Pagamento</h2>
    <h3>5.1. Comissão</h3>
    <p>O afiliado receberá uma comissão de X% sobre cada venda realizada através do seu link de afiliado. A taxa de comissão é determinada pelo Portal JE e pode ser alterada a qualquer momento, com aviso prévio aos afiliados.</p>

    <h3>5.2. Pagamento</h3>
    <p>Os pagamentos serão efetuados mensalmente, desde que o saldo mínimo de comissão seja de R$ Y,00. Caso o saldo não atinja o mínimo, o valor será acumulado para o mês seguinte.</p>

    <h3>5.3. Impostos</h3>
    <p>O afiliado é responsável por todas as obrigações fiscais relativas às comissões recebidas.</p>

    <h2>6. Políticas de Uso e Privacidade</h2>
    <p>Os afiliados devem respeitar todas as políticas de privacidade e proteção de dados estabelecidas pelo Portal JE e garantir que qualquer informação coletada de clientes potenciais seja tratada de acordo com a LGPD (Lei Geral de Proteção de Dados).</p>

    <h2>7. Cancelamento e Rescisão</h2>
    <h3>7.1. Cancelamento Voluntário</h3>
    <p>O afiliado pode encerrar sua participação no programa a qualquer momento, enviando uma notificação por escrito ao Portal JE.</p>

    <h3>7.2. Rescisão pelo Portal JE</h3>
    <p>O Portal JE se reserva o direito de rescindir o acordo de afiliação a qualquer momento, com ou sem justa causa, mediante notificação por escrito.</p>

    <h3>7.3. Consequências da Rescisão</h3>
    <p>Após o término da afiliação, o afiliado deve cessar imediatamente toda a promoção dos cursos e remover todos os materiais promocionais do Portal JE de seus canais.</p>

